using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Player : Character
{
    private Enemy _enemy;
    private float _randomAttack;

    /// <summary>
    /// 1. Init: 초기화 기능
    /// 1) Subject에 Observer로 등록
    /// 2) _myName, _myHp, _myDamage 초기화
    /// 3) _myName은 무조건 "Player"로 할 것
    /// 4) _myHp, _myDamage는 100, 20으로 각각 초기화 (권장 사항)
    /// </summary>
    protected override void Init()
    {
        base.Init();

        GameManager.Instance().AddCharacter(this);
        _myName = "Player";
        _myHp = 100;
        _myDamage = 20;
    }

    private void Awake()
    {
        Init();
    }

    /// <summary>
    /// 1) _enemy가 할당이 안됐다면,
    /// 2) GameObject.FindWithTag 이용해서 _enemy 할당
    /// </summary>
    private void Start()
    {
        //내가 필요한건 Enemy의 GetHit이므로, getComponent해서 Enemy까지 접근해야 함
        _enemy = GameObject.FindGameObjectWithTag("Enemy").GetComponent<Enemy>();
    }

    /// <summary>
    /// Attack:
    /// 1) Player는 30%의 확률로 공격력이 더 높은 공격을 가할 것
    /// 2) _randomAttack = Random.Range(0,10); 으로 랜덤 변수 생성
    ///   -> 0~9 까지의 정수 중 하나를 랜덤으로 할당받음.
    /// 3) _randomAttack 이용해서 30% 확률로 기존 공격력보다 10 높은 공격 실행
    /// 4) 이때는 AttackMotion() 말고 SpecialAttackMotion() 호출할 것
    ///    + Debug.Log($"{_myName} Special Attack!"); 추가
    /// 5) 70% 확률로 하는 일반 공격은 Character에 써있는 주석과 동일
    /// </summary>
    public override void Attack()
    {
        

        if (_whoseTurn == _myName && _isFinished == false)
        {
            _randomAttack = Random.Range(0, 10);
            if (_randomAttack < 3)
            {
                _myDamage += 10;
                Debug.Log($"{_myName} Special Attack!");
                _enemy.GetHit(_myDamage);
                SpecialAttackMotion();
                

            }

            else
            {
                _enemy.GetHit(_myDamage);
                AttackMotion();
            }
        }
    }

    public override void GetHit(float damage)
    {
        base.GetHit(damage);

        StartCoroutine(HpBarDelay());
        
        
    }


    //HP bar UI

    public delegate void HpHandler(float hp);
    HpHandler _hpHandler;


    public void AddObserver(HpInterface characterHpBar)
    {
        _hpHandler += characterHpBar.HpOnNotify;
    }

    public void HpNotify(float hp)
    {
        _hpHandler(hp);
    }



    IEnumerator HpBarDelay()
    {

        yield return new WaitForSeconds(2f);
        HpNotify(_myHp);

    }






}
